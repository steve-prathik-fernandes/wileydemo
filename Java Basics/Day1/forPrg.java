class forPrg{
    public static void main(String args[]){
        for(int i=0;i<10;i++){
            for(int j=0;j<10;j++){
                System.out.println(i+"----"+j);
            }
        }
        int n=10,i=0;
        while(n!=0){
            System.out.println(i++);
            n--;
        }
    }
}